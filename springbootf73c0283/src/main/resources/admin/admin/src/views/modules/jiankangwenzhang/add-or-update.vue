	import { 
		isIntNumer,
	} from "@/utils/validate";
	export default {
		data() {
			var validateIntNumber = (rule, value, callback) => {
				if(!value){
					callback();
				} else if (!isIntNumer(value)) {
					callback(new Error("请输入整数"));
				} else {
					callback();
				}
			};
			return {
				id: '',
				type: '',
			
			
				ro:{
					biaoti : false,
					jiankangfenlei : false,
					fengmiantupian : false,
					fabushijian : false,
					faburen : false,
					laiyuan : false,
					neirong : false,
					thumbsupnum : false,
					crazilynum : false,
					clicktime : false,
					clicknum : false,
					discussnum : false,
					storeupnum : false,
				},
			
				ruleForm: {
					biaoti: '',
					jiankangfenlei: '',
					fengmiantupian: '',
					fabushijian: '',
					faburen: '管理员',
					laiyuan: '',
					neirong: '',
					clicktime: '',
				},
				jiankangfenleiOptions: [],

				rules: {
					biaoti: [
						{ required: true, message: '标题不能为空', trigger: 'blur' },
					],
					jiankangfenlei: [
					],
					fengmiantupian: [
