<script>
	import wPicker from "@/components/w-picker/w-picker.vue";
	import xiaEditor from '@/components/xia-editor/xia-editor';
	import multipleSelect from "@/components/momo-multipleSelect/momo-multipleSelect";
	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				huanzhezhanghao: '',
				huanzhexingming: '',
				nianling: '',
				xingbie: '',
				chuangjianshijian: '',
				danganmingcheng: '',
				touxiang: '',
				danganneirong: '',
				gengxinshijian: '',
				wangbingshi: '',
				guominshi: '',
				jiazubingshi: '',
				},
				// 登录用户信息
				user: {},
				ro:{
				   huanzhezhanghao : false,
				   huanzhexingming : false,
				   nianling : false,
				   xingbie : false,
				   chuangjianshijian : false,
				   danganmingcheng : false,
				   touxiang : false,
				   danganneirong : false,
				   gengxinshijian : false,
				   wangbingshi : false,
				   guominshi : false,
				   jiazubingshi : false,
				},
				virtualPay: false,
			}
		},
		components: {
			wPicker,
			xiaEditor,
			multipleSelect,
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
			if(options.virtualPay){
				this.virtualPay = true
			}
			this.ruleForm.chuangjianshijian = this.$utils.getCurDate();
			this.ruleForm.gengxinshijian =  this.$utils.getCurDateTime();
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取
			this.ruleForm.huanzhezhanghao = this.user.huanzhezhanghao
			this.ro.huanzhezhanghao = true;
			this.ruleForm.huanzhexingming = this.user.huanzhexingming
			this.ro.huanzhexingming = true;
			this.ruleForm.nianling = this.user.nianling
			this.ro.nianling = true;
			this.ruleForm.xingbie = this.user.xingbie
			this.ro.xingbie = true;
			this.ruleForm.touxiang = this.user.touxiang
			this.ro.touxiang = true;



			// 如果有登录，获取登录后保存的userid
			this.ruleForm.userid = uni.getStorageSync("appUserid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = Number(options.refid);
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`jiankangdangan`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='huanzhezhanghao'){
						this.ruleForm.huanzhezhanghao = obj[o];
						this.ro.huanzhezhanghao = true;
						continue;
					}
					if(o=='huanzhexingming'){
						this.ruleForm.huanzhexingming = obj[o];
						this.ro.huanzhexingming = true;
						continue;
					}
					if(o=='nianling'){
						this.ruleForm.nianling = obj[o];
						this.ro.nianling = true;
						continue;
					}
					if(o=='xingbie'){
						this.ruleForm.xingbie = obj[o];
						this.ro.xingbie = true;
						continue;
					}
					if(o=='chuangjianshijian'){
						this.ruleForm.chuangjianshijian = obj[o];
						this.ro.chuangjianshijian = true;
						continue;
					}
					if(o=='danganmingcheng'){
						this.ruleForm.danganmingcheng = obj[o];
						this.ro.danganmingcheng = true;
						continue;
					}
					if(o=='touxiang'){
						this.ruleForm.touxiang = obj[o].split(",")[0];
						this.ro.touxiang = true;
						continue;
					}
					if(o=='danganneirong'){
						this.ruleForm.danganneirong = obj[o];
						this.ro.danganneirong = true;
						continue;
					}
					if(o=='gengxinshijian'){
						this.ruleForm.gengxinshijian = obj[o];
						this.ro.gengxinshijian = true;
						continue;
					}
					if(o=='wangbingshi'){
						this.ruleForm.wangbingshi = obj[o];
						this.ro.wangbingshi = true;
						continue;
